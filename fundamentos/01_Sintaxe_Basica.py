# Variáveis
nome
mensagem

# Entrada de dados
nome = input("Digite seu nome: ")

# Processamento usando condicional
if nome:
    mensagem = f"Olá, {nome}! Bem-vindo ao Python!"
else:
    mensagem = "Olá! Bem-vindo ao Python!"

# Saída de dados
print(mensagem)