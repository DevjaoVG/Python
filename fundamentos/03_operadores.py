"""
Demonstra o uso de operadores aritméticos, de comparação e lógicos em Python.
"""

# Operadores aritméticos
adicao = 10 + 5  # Adição: 15
subtracao = 10 - 5  # Subtração: 5
multiplicacao = 10 * 5  # Multiplicação: 50
divisao = 10 / 5  # Divisão: 2.0
modulo = 10 % 3  # Resto da divisão: 1
exponenciacao = 2 ** 3  # Potenciação: 8

# Operadores de comparação
igual = 10 == 5  # Igual a
diferente = 10 != 5  # Diferente de
maior = 10 > 5  # Maior que

